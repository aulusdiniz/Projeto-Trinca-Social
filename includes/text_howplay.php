COMO JOGAR
<p>
1. Faça seu cadastro no site do jogo, criando seu perl;
</p>
<p>
2. Clique em ENTRAR ou JOGAR AGORA para começar um novo jogo ou retornar um jogo anterior.
Você também precisará ser cadastrado para acessar as trincas publicadas e participar da votação;
</p>
<p>
3. Ao iniciar o jogo, você receberá seis cartas e manterá sempre o mesmo número de cartas;
</p>
<p>
4. Você poderá clicar no monte para comprar novas cartas e arrastá-las para substituir por qualquer
outra carta . Também poderá substituir pelas cartas anteriormente descartadas, sempre mantendo
seis cartas em sua área. Estas ações poderão ser repetidas várias vezes até que você encontre
relações entre elas na lógica do jogo;
</p>
<p>
5. Quando você a TRINCA é formada, arraste-a para o campo de trincas e justifique a relação entre
as cartas utilizando, se possível, as mesmas palavras das cartas. caso você prefira usar diferentes
palavras relacione-a com a carta. Obs. As cartas em branco poderão ser dispostas na trinca como
você convir, basta criar um novo termo que se relacione a ela;
</p>
<p>
6. Após a justicativa, releia e conrme para a publicação da mesma na rede ou retorne e edite
novamente caso queira deixá-la ainda mais clara para o público;
</p>
<p>
7. A trinca será publicada na rede e estará disponível para votação durante 7 dias. acompanhe as
discussões e comente com outros jogadores argumentando seu ponto de vista. Os jogadores
participantes poderão concordar ou discordar com sua trinca durante o peíodo que poderá ser
prorrogado caso não haja o mínimo de 3 votos;
</p>
<p>
8. Faça quantas trincas quiser e publique -as na rede. Após 15 trincas aprovadas, a sua área de
atuação poderá ser expandida e você receberá orientações para criar uma ação a ser desempenhada
no jogo ou em sua vida;
</p>
<p>
ACORDOS DO JOGO
Este jogo preza pela atuação positiva entre as pessoas e ações gentis no mundo e não permitirá
agressões verbais ou termos que violam a moral
</p>
