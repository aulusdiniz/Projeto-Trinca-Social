<p>
TRINCA SOCIAL é um jogo coletivo que propõe uma rede de diálogos sobre problemas sociais vividos em
sociedade e estimula ações como participação pública nas cidades. É um jogo de cartas digitais ou impressas
onde o jogador estabelece uma ordem onde relaciona as cartas como CAUSA DO PROBLEMA - PROBLEMA -
SOLUÇÃO DO PROBLEMA e publica uma justicativa sobre como pensa o assunto. A partir daí, os diferentes
pontos de vista são publicados numa rede gentil que concorda ou discorda das trincas, sempre na busca do
enriquecimento dos diálogos em torno da solução dos problemas. Trinca Social preza pela formação de uma
rede gentil onde boas ações são pontuadas no mapa e ampliação de ideias e pessoas interessadas em resolver
problemas sociais.
</p>
<p>
OBJETIVO DO JOGO
• Refletir sobre problemas, causas e consequências, tentando encontrar soluções viáveis para questões sociais
vivenciadas na sociedade;
• Trabalhar o senso crítico por meio da reflexão de causas e efeitos;
• Estimular a comunicação para a resolução dos conflitos;
• Estimular a capacidade de argumentação e organização de ideias em defesa de opiniões pessoais;
• Estimular ações e participação pública nas cidades criando uma plataforma de encontro e troca de ideias para
a resolução de problemas.
</p>
<p>
RECURSOS
Baralhos de diferentes áreas de atuação com cartas que poderão ser organizadas pelo jogador que elegerá
problemas, causas e soluções, disponibilizando opiniões na rede;
Rede social de diálogos e reflexões sobre situações-problema, propondo possíveis soluções para as mesmas.
</p>
<p>
PARTICIPANTES
Inicialmente, cada jogador formará e publicará individualmente suas trincas porém ela só será aprovada se
houver no mínimo 3 participantes que votarem dentro do prazo de 7 dias após a publicação da trinca. Caso a
trinca não for votada dentro do prazo de 7 dias, este poderá ser prorrogado por mais 7 dias. Não será limitado o
número máximo de pessoas que poderá participar da votação.
</p>

